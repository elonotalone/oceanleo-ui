/** 站点的内置 prompt 卡片（没配置的站回退通用集）。 */
export function promptCardsForSite(siteId: string): PromptCard[] {
  return PROMPT_LIBRARY[(siteId || "").trim()] || GENERIC_PROMPTS;
}

// --- 用户自建卡片（localStorage 持久化，按站隔离） ---------------------------
const CUSTOM_KEY_PREFIX = "oceanleo_home_prompts:";
const CUSTOM_KEY = (siteId: string) => `${CUSTOM_KEY_PREFIX}${siteId || "default"}`;

export function loadCustomPromptCards(siteId: string): PromptCard[] {
  if (typeof window === "undefined") return [];
  try {
    const raw = localStorage.getItem(CUSTOM_KEY(siteId));
    const arr = raw ? JSON.parse(raw) : [];
    if (!Array.isArray(arr)) return [];
    return arr
      .filter((c) => c && typeof c === "object" && c.id && c.title && c.prompt)
      .map((c) => ({ ...c, custom: true }) as PromptCard);
  } catch {
    return [];
  }
}

export function saveCustomPromptCards(siteId: string, cards: PromptCard[]): void {
  if (typeof window === "undefined") return;
  try {
    localStorage.setItem(
      CUSTOM_KEY(siteId),
      JSON.stringify(cards.map((c) => ({ ...c, custom: true }))),
    );
  } catch {
    /* storage full / private mode — 忽略 */
  }
}

/**
 * 聚合读取【当前 origin 下所有站】的用户自建 prompt 卡片（宗旨 v12，2026-07-04）。
 * 扫描所有 `oceanleo_home_prompts:*` localStorage key 合并去重（按 id）——让「用户在
 * 某站首页新建的 prompt」自动出现在 `oceanleo.com/playground` 的 prompt 专区。
 * 注意：localStorage 按 origin 隔离，只能聚合同 origin（同一个 *.oceanleo.com 域或主站）
 * 下的自建卡；真正的跨子站 → 主站同步需后端用户级存储（本次不做）。
 */
export function loadAllCustomPromptCards(): PromptCard[] {
  if (typeof window === "undefined") return [];
  const out: PromptCard[] = [];
  const seen = new Set<string>();
  try {
    for (let i = 0; i < localStorage.length; i++) {
      const key = localStorage.key(i);
      if (!key || !key.startsWith(CUSTOM_KEY_PREFIX)) continue;
      const siteId = key.slice(CUSTOM_KEY_PREFIX.length);
      for (const c of loadCustomPromptCards(siteId)) {
        if (seen.has(c.id)) continue;
        seen.add(c.id);
        out.push(c);
      }
    }
  } catch {
    /* 忽略 */
  }
  return out;
}
